### Задача 1. Аналитик собрал статистику за 36 прошедших месяцев, согласно которой вложение в акцию компании АВС 
# позволило за месяц заработать 15% в 1 случае из 36 проанализированных месяцев, 10% - в 2 случаях, 5% - в 3 случаях, 
# 0% в 23 случаях, потерять 5% в 6 случаях, и потерять 10% в оставшихся случаях. Определить ожидаемое значение доходности
# инвестиции. ###


# Вероятности событий
probabilities = [1/36, 2/36, 3/36, 23/36, 6/36, 1/36]

# Доходности при соответствующих событиях
returns = [0.15, 0.10, 0.05, 0, -0.05, -0.10]

# Вычисление ожидаемой доходности
expected_return = sum(p * r for p, r in zip(probabilities, returns))

# Вывод результата
print(f"Ожидаемая доходность: {expected_return * 100:.2f}%")

### Вывод : Ожидаемая доходность: 0.28% ###

