### Задача 3. В инвестиционном анализе есть т.н. показатель Шарпа, который характеризует эффективность инвестиции в фонд. 
# Он рассчитывается как отношение средней доходности к риску (волатильности). Рассчитайте этот коэффициент для условий 
# задачи 2. ### 

import numpy as np

# Годовые доходности портфеля из задачи 2
annual_returns = [0.05, -0.02, 0.12, 0.07, -0.04, 0.11, 0.09]

# Рассчитываем среднюю доходность и стандартное отклонение
average_return = np.mean(annual_returns)
volatility = np.std(annual_returns)

# Задаем безрисковую ставку (пример: 2%)
risk_free_rate = 0.02

# Рассчитываем коэффициент Шарпа
sharpe_ratio = (average_return - risk_free_rate) / volatility

# Выводим результат
print(f"Коэффициент Шарпа: {sharpe_ratio:.4f}")


### Вывод : Коэффициент Шарпа: 0.5934 ###

