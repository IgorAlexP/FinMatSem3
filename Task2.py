### Задача 2. Инвестиционный фонд за 7 предыдущих лет заработал следующие годовые доходности:
# +5%; -2%; +12%; +7%; -4%; +11%; +9%. Если предположить, что в среднем фонд будет зарабатывать такую же доходность, 
# как и по итогам прошедших 7 лет (в смысле геометрической доходности), и вложить $100 тыс. в этот фонд, то какой 
# ожидаемый результат будет через 5 лет? ###

# Входные данные
initial_investment = 100000
annual_returns = [0.05, -0.02, 0.12, 0.07, -0.04, 0.11, 0.09]

# Рассчитываем геометрическую доходность
geometric_return = 1
for return_rate in annual_returns:
    geometric_return *= 1 + return_rate

# Рассчитываем будущую стоимость через 5 лет
future_value = initial_investment * geometric_return

# Выводим результат
print(f"Ожидаемый результат через 5 лет: ${future_value:.2f}")


### Ожидаемый результат через 5 лет: $143231.28 ###
